void Rotate_Motor(MotorNum m, int pwm) {
	switch(m) {
		case Front_Left:
			__HAL_TIM_SET_COMPARE(&htim8, TIM_CHANNEL_1, pwm);
			break;
		case Front_Right:
			__HAL_TIM_SET_COMPARE(&htim8, TIM_CHANNEL_2, pwm);
			break;
		case Back_Left:
			__HAL_TIM_SET_COMPARE(&htim8, TIM_CHANNEL_3, pwm);
			break;
		case Back_Right:
			__HAL_TIM_SET_COMPARE(&htim8, TIM_CHANNEL_4, pwm);
			break;
		case Pantagraph:
			__HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, pwm);
			break;
		case AR_Left:
			__HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_3, pwm);
			break;
		case AR_Right:
			__HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_2, pwm);
			break;
	}
}

void Move_Button(uint16_t button) {
	if (button) {
		uint16_t bit = 1;
		for (int i = 0; i < BUTTON_NUM; ++i) {
			if (button & bit) {
				switch (i) {
					case Up :
						break;
					case Right :
						break;
					case Down :
						break;
					case Left :
						break;
					case Square :
						mode = Auto;
						break;
					case Cross :
						mode = Manual;
						break;
					case Circle :
						Rotate_Motor(AR_Left, servo[0].MAX);
						break;
					case Triangle :
						break;
					case L1 :
						break;
					case R1:
						break;
					case L2:
						break;
					case R2:
						break;
				}
			}
			bit <<= 1;
		}
	}
	else {
		Rotate_Motor(AR_Left, servo[0].STOP);
	}
}

